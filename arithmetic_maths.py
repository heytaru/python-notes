# Basic arithmetic operator
# + Addition operator
print(f"3 + 5 = {3 + 5}")

# - Subtraction operator
print(f"3 - 5 = {3 - 5}")

# * multiplication operator
print(f"3 * 5 = {3 * 5}")

# / divison operator
print(f"3 / 5 = {3 / 5}")

# % modulus operator
print(f"3 % 5 = {3 % 5}")

# exponents operator
print(f"2 ** 3 = {2 ** 3}")
